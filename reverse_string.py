# python3
# Write a function that takes a string as input and returns the string reversed.

# Example:
# Input: s = 'hello'
# Output: olleh

# My solution
# it is simple to solve this problem with python
class Solution(object):
    def reverseString(self, s):
        """
        :type s: str
        :rtype: str
        """
        return s[::-1]
